package teachers.model;

public class TEBean {
	private int tnum;
	private String tid;
	private String tname;
	private String t_image;
	private String subject;
	private String visa;
	private String tterm;
	private String twageratio;
	private String introduction;
	
	
	public int getTnum() {
		return tnum;
	}
	public void setTnum(int tnum) {
		this.tnum = tnum;
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getT_image() {
		return t_image;
	}
	public void setT_image(String t_image) {
		this.t_image = t_image;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getVisa() {
		return visa;
	}
	public void setVisa(String visa) {
		this.visa = visa;
	}
	public String getTterm() {
		return tterm;
	}
	public void setTterm(String tterm) {
		this.tterm = tterm;
	}
	public String getTwageratio() {
		return twageratio;
	}
	public void setTwageratio(String twageratio) {
		this.twageratio = twageratio;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	
	
}
